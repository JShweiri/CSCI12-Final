          <h1>Status</h1>
            <p>In 2011, mantas became strictly protected in international waters because of their inclusion in the Convention on Migratory Species of Wild Animals. The CMS is an international treaty organization concerned with conserving migratory species and habitats on a global scale. Although individual nations were already protecting manta rays, the fish often migrate through unregulated waters, putting them at increased risk from overfishing. The IUCN declared M. birostris to be 'Vulnerable with an elevated risk of extinction' in November 2011.
</p><p>
In the same year, M. alfredi was also classified as 'Vulnerable' with local populations of fewer than 1000 individuals and little or no interchange between subpopulations. The Manta Trust is a UK-based charity dedicated to research and conservation efforts for manta rays. The organization's website is also an information resource for manta conservation and biology.
</p><p>
Besides these international initiatives, some countries are taking their own actions. New Zealand has banned the taking of manta rays since the introduction of the Wildlife Act in 1953. In June 1995, the Maldives banned the export of all ray species and their body parts, effectively putting a stop to manta fishing as there had not previously been a fishery for local consumption. The government reinforced this in 2009 with the introduction of two marine protected areas. In the Philippines, the taking of mantas was banned in 1998, but this was overturned in 1999 under pressure from local fishermen. Fish stocks were surveyed in 2002, and the ban was reintroduced. The taking or killing of mantas in Mexican waters was prohibited in 2007. This ban may not be strictly enforced, but laws are being more rigidly applied at Isla Holbox, an island off the Yucat√°n Peninsula, where manta rays are used to attract tourists.
