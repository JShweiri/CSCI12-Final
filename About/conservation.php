          <h1>Threats</h1>
            <p>The greatest threat to manta rays is overfishing. M. birostris is not evenly distributed over the oceans, but is concentrated in areas that provide the food resources it requires, while M. alfredi is even more localized. Their distributions are thus fragmented, with little evidence of intermingling of subpopulations. Because of their long lifespans and low reproductive rate, overfishing can severely reduce local populations with little likelihood that individuals from elsewhere will replace them.
</p>
<p>
Both commercial and artisanal fisheries have targeted mantas for their meat and products. They are typically caught with nets, trawls and harpoons. Mantas were once captured by fisheries in California and Australia for their liver oil and skin; the latter were used as abrasives. Their flesh is edible and is consumed in some countries, but is unattractive compared to other fish. Demand for their gill rakers, the cartilaginous structures protecting the gills, has recently entered Chinese medicine. To fill the growing demand in Asia for gill rakers, targeted fisheries have developed in Philippines, Indonesia, Mozambique, Madagascar, India, Pakistan, Sri Lanka, Brazil and Tanzania. Each year, thousands of manta rays, primarily M. birostris, are caught and killed purely for their gill rakers. A fisheries study in Sri Lanka and India estimated that over 1000 were being sold in the country's fish markets each year. By comparison, M. birostris populations at most of the key aggregation sites around the world are estimated to have significantly fewer than 1000 individuals. Targeted fisheries for manta rays in the Gulf of California, the west coast of Mexico, India, Sri Lanka, Indonesia, and the Philippines have reduced populations in these areas dramatically.
</p><p>
Manta rays are subject to other anthropogenic threats. Because mantas must swim constantly to flush oxygen-rich water over their gills, they are vulnerable to entanglement and subsequent suffocation. Mantas cannot swim backwards and, because of their protruding cephalic fins, are prone to entanglement in fishing lines, nets / ghost nets, and even loose mooring lines. When snared, mantas often attempt to free themselves by somersaulting, tangling themselves further. Loose, trailing line can wrap around and cut its way into its flesh, resulting in irreversible injury. Similarly, mantas become entangled in gill nets designed for smaller fish. Some mantas are injured by collision with boats, especially in areas where they congregate and are easily observed. Other threats or factors that may affect manta numbers are climate change, tourism, pollution from oil spills, and the ingestion of microplastics.
